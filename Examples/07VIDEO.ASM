.model tiny

;-----------------------------------------------------------------------------

videoseg	= 0b800h
probel		= 4e00h
x1		= 10
x2		= 70
y1              = 5
y2              = 20

;-----------------------------------------------------------------------------
.code
org 100h

start:		mov ax, videoseg
		mov es, ax
		mov ax, probel
		mov di, (y1 * 80 + x1) * 2
		mov cx, x2 - x1         ; Window width
		mov si, 0000h		; si -> bx. if si = bx, exit 
		mov bx, y2 - y1         ; Window height
x1_to_x2:	stosw			; mov es:[di], ax  /  inc di
		loop x1_to_x2

y1_to_y2:	add di, (80d - (x2 - x1)) * 2
		inc si
		mov cx, x2 - x1
		cmp si, bx
		jne x1_to_x2

		mov ax, 4c00h
		int 21h
end start

