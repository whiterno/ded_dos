.model tiny

.code
org 100h
start:		mov bx, 0b800h		; B800 - адрес видеопамяти            
		mov es, bx              ; 
		mov bx, 0
		mov byte ptr es:[bx], 21h ; Помещение 21h (!) в лв угол экрана

		mov ax, 4c00h           ; выход
		int 21h
end start

