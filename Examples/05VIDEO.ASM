.model tiny

;-----------------------------------------------------------------------------

x = 2
y = 3

;-----------------------------------------------------------------------------
.code
org 100h
start:		mov ax, 0b800h
		mov es, ax
		mov al, y	; al = y
		mov ah, 80d	; ah = 80
		mul ah		; ax = ah * al
		add ax, x	; ax = ax + x
		shl ax, 1	; ax = ax * 2
		mov bx, ax	
		mov es:[bx], 4e21h

		mov ax, 4c00h
		int 21h
end start
